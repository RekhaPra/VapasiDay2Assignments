package javaassignmentsday1;

public class PerimeterAndAreaRectangle {
    public static void main(String[] args){
        int width= 5;
        int height= 7;
        int perimeter = 2* (width+height);
        int area = width*height;
        System.out.println("Perimeter of rectangle (width 2 & height 3) :" + perimeter);
        System.out.println("Area of rectangle (width 2 & height 3) :" + area);

    }
}
